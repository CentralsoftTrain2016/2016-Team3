package domain.valueo;

public class TemplateID extends Value{

	private String templateID;	// テンプレートID

	/* コンストラクタ */
	public TemplateID(String templateID){
		super();
		this.templateID = templateID;
	}

	// テンプレートIDを取得
	public String getTemplateID(){
		return templateID;
	}
	public String setTemplateID(){
		return templateID;
	}
}
