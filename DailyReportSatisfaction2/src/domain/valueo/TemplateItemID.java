package domain.valueo;

public class TemplateItemID extends Value{

	private String templateItemID;		// テンプレートアイテムID

	/* コンストラクタ */
	public TemplateItemID(String templateItemID){
		super();
		this.templateItemID = templateItemID;
	}

	// テンプレートアイテムIDを取得
	public String getTemplateItemID(){
		return templateItemID;
	}

}
