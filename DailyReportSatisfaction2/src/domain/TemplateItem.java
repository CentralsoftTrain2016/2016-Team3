package domain;

public class TemplateItem {
	private	String	TEMPLATEITEMID	;
	private	String	SHEETTAB;
	private	String	ITEMNAME;
	private	String	PARENTTEMPLATEITEMID;
	private	String	TEMPLATEID;

	public String getTEMPLATEITEMID() {
		return TEMPLATEITEMID;
	}
	public void setTEMPLATEITEMID(String tEMPLATERULEID) {
		TEMPLATEID = tEMPLATERULEID;
	}

	public String getSHEETTAB() {
		return SHEETTAB;
	}
	public void setSHEETTAB(String sHEETTAB) {
		SHEETTAB = sHEETTAB;
	}

	public String getITEMNAME() {
		return ITEMNAME;
	}
	public void setITEMNAME(String iTEM) {
		ITEMNAME = iTEM;
	}

	public String getPARENTTEMPLATEITEMID() {
		return PARENTTEMPLATEITEMID;
	}
	public void setDEPARTMENTID(String pARENTTEMPLATEITEMID) {
		PARENTTEMPLATEITEMID = pARENTTEMPLATEITEMID;
	}

	public String getTEMPLATEID() {
		return TEMPLATEID;
	}
	public void setTEMPLATEID(String tEMPLATEID) {
		TEMPLATEID = tEMPLATEID;
	}

	public String toString() {
		return this.ITEMNAME;
	}
}
