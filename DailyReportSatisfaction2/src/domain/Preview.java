package domain;

import domain.valueo.IndentLevel;
import domain.valueo.ItemName;
import domain.valueo.TemplateID;
import domain.valueo.TemplateItemID;
import domain.valueo.Text;
import domain.valueo.UserID;

public class Preview {
	private UserID userID;
	private TemplateID templateID;
	private TemplateItemID templateItemID;
	private ItemName itemName;
	private Text text;
	private IndentLevel indentLevel;

	public UserID getUserID() {
		return userID;
	}

	public void setUserID(UserID userID) {
		this.userID = userID;
	}

	public TemplateID getTemplateID() {
		return templateID;
	}

	public void setTemplateID(TemplateID templateID) {
		this.templateID = templateID;
	}

	public TemplateItemID getTemplateItemID() {
		return templateItemID;
	}

	public void setTemplateItemID(TemplateItemID templateItemID) {
		this.templateItemID = templateItemID;
	}

	public ItemName getItemName() {
		return itemName;
	}

	public void setItemName(ItemName itemName) {
		this.itemName = itemName;
	}

	public Text getText() {
		return text;
	}

	public void setText(Text text) {
		this.text = text;
	}

	public IndentLevel getIndentLevel() {
		return indentLevel;
	}

	public void setIndentLevel(IndentLevel indentLevel) {
		this.indentLevel = indentLevel;
	}

}
